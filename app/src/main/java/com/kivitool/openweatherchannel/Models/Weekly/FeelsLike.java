package com.kivitool.openweatherchannel.Models.Weekly;

public class FeelsLike{
	private double eve;
	private double night;
	private double day;
	private double morn;

	public double getEve(){
		return eve;
	}

	public double getNight(){
		return night;
	}

	public double getDay(){
		return day;
	}

	public double getMorn(){
		return morn;
	}
}
